#! /usr/bin/perl -w
use strict;
use warnings;
$^W = 1;
$| = 1; # Turn on auto-flush

my @movie_entries = ();
while (<DATA>) {
    chop;
    my ($movie_id,$num_predictions) = split /\,/;
    push(@movie_entries, [$movie_id,$num_predictions]);
}

my $num_movies = scalar(@movie_entries);
my $submission_filename = $ARGV[0];

my $movie_index = -1;
my $num_predictions = 0;
my ($movie_id, $expected_movie_id, $required_predictions);

open SUBMISSION, "<$submission_filename" or die "Unable to open $submission_filename! ($!)";
while (<SUBMISSION>) {
    s/\cJ//g; # lose line terminators anywhere in the string
    s/\cM//g; # lose line terminators anywhere in the string
    my $prediction = $_;
    if ($prediction =~ /:/) {
        die "Line $.: Insufficient predictions for movie $movie_id"  if $movie_index != -1 and $num_predictions < $required_predictions;

        ($movie_id) = split /:/, $prediction;
        die "Line $.: Non-numeric movie id" unless $movie_id =~ /^\d+$/; # Is it an integer?
        $movie_index++;
        die "Line $.: Unexpected movie id $movie_id" if $movie_index >= $num_movies;

        ($expected_movie_id,$required_predictions) = @{$movie_entries[$movie_index]};
        die "Line $.: Unexpected movie id $movie_id, expected $expected_movie_id" unless $expected_movie_id == $movie_id;
        $num_predictions = 0;
    } else {
        # Must be a prediction line
        die "Line $.: No initial movie specified" if $movie_index == -1; # Didn't get set up at the beginning
        die "Line $.: Unacceptable prediction for movie $movie_id"
          unless $prediction =~ /^[\d\.]+$/ # Is it a (FP) number?
            and $prediction >= 1.0 and $prediction <= 5.0;
        die "Line $.: Too many predictions for movie $movie_id" if $num_predictions >= $required_predictions;
        $num_predictions++;
    }
}
die "EOF: Insufficient predictions specified for movie $movie_id"  if $movie_index != -1 and $num_predictions < $required_predictions;
$movie_index++;  # adjust count of actual movies seen
die "EOF: Insufficient movies predicted ($movie_index vs $num_movies)" if $movie_index != $num_movies;
close SUBMISSION;

print "OK!\n";

